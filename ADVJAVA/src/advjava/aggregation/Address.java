package advjava.aggregation;

public class Address {
	int dno ;
	int pincode ;
	String street ;
	String state;
	String country ;
	public Address(int dno, int pincode, String street, String state, String country) {
		super();
		this.dno = dno;
		this.pincode=pincode;
		this.street = street;
		this.state = state;
		this.country = country;
	}
	
}
