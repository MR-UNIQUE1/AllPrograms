package com.java.cj.chapter1;

public class LoopFor {

	public static void main(String[] args) {
		for (int i = 1; i <=10; i++) {
			System.out.println("good morning mama...."+i);
		}
		for (int i = 15; i >=10; i--) {
			System.out.println("good morning sunil.........."+i);
		}
		for (int i = 0; i <=20; i+=5) {
			System.out.println("goog evening ranu......."+i);
		}
		for (int i = -7; i <=-2; i++) {
			System.out.println("good Afternoon dei........."+i);
		}
	}

}
